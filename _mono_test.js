// 忠实移植 main.js 的 uMono GLSL 分支为纯函数，便于无 WebGL 环境测试。
// GLSL: l=dot(c,(.2126,.7152,.0722)); c=mix(c,vec3(l),uMono)
function applyMono(c, v){
  const l = 0.2126*c[0] + 0.7152*c[1] + 0.0722*c[2];
  return [c[0]*(1-v)+l*v, c[1]*(1-v)+l*v, c[2]*(1-v)+l*v];
}
function eq3(a, b){ return a.length===3 && a.every((x,i)=> Math.abs(x-b[i]) < 1e-6); }
let pass=0, fail=0;
function ok(n, c){ if(c){ pass++; } else { fail++; console.log('  FAIL:', n); } }

ok('v=0 不变 [0.2,0.5,0.8]', eq3(applyMono([0.2,0.5,0.8],0), [0.2,0.5,0.8]));
ok('v=0 不变 [1,0,0.5]', eq3(applyMono([1,0,0.5],0), [1,0,0.5]));
// l = 0.2126*0.2+0.7152*0.5+0.0722*0.8 = 0.04252+0.3576+0.05776 = 0.45788
ok('v=1 纯灰度 [0.2,0.5,0.8]', eq3(applyMono([0.2,0.5,0.8],1), [0.45788,0.45788,0.45788]));
ok('v=1 三通道相等(灰度)', (()=>{ const c=applyMono([0.1,0.4,0.9],1); return Math.abs(c[0]-c[1])<1e-9 && Math.abs(c[1]-c[2])<1e-9; })());
// v=0.5: c' = c*0.5 + l*0.5; l=0.45788 → [0.2*0.5+0.45788*0.5, 0.5*0.5+0.45788*0.5, 0.8*0.5+0.45788*0.5]
// = [0.32894, 0.47894, 0.62894]
ok('v=0.5 半去色 [0.2,0.5,0.8]', eq3(applyMono([0.2,0.5,0.8],0.5), [0.32894,0.47894,0.62894]));
ok('v=1 灰度值=Rec709亮度 [0.3,0.6,0.1]', (()=>{ const l=0.2126*0.3+0.7152*0.6+0.0722*0.1; const c=applyMono([0.3,0.6,0.1],1); return Math.abs(c[0]-l)<1e-6; })());
ok('v=0.25 微去色仍彩色', (()=>{ const c=applyMono([0.9,0.2,0.1],0.25); return Math.abs(c[0]-c[2])>0.1; })());
ok('v=1 纯红 [1,0,0] => 亮度 0.2126 灰度', eq3(applyMono([1,0,0],1), [0.2126,0.2126,0.2126]));
ok('v=1 纯绿 [0,1,0] => 亮度 0.7152 灰度', eq3(applyMono([0,1,0],1), [0.7152,0.7152,0.7152]));
ok('v=1 纯蓝 [0,0,1] => 亮度 0.0722 灰度', eq3(applyMono([0,0,1],1), [0.0722,0.0722,0.0722]));
ok('亮度公式独立验证 [0.2,0.5,0.8]', Math.abs((0.2126*0.2+0.7152*0.5+0.0722*0.8) - 0.45788) < 1e-5);

console.log(`mono: ${pass} pass, ${fail} fail`);
process.exit(fail ? 1 : 0);
